Poštovana/i, 

sve navedeno bi mogli voditi kroz opciju 
Održavanje | Posebne obrade | Garancija povratnog otkupa 
u kojoj za: 
- garanta unosite kupca/dobavljača;
- OT i CV u polje Tip garancija (padajuće polje čije vrijednosti se popunjavaju u posebnom šifrantu ID šifranta = TIP_GARANCIJE)
Unos garancije povratnog otkupa se može podesiti da bude kod unosa ugovora nakon trećeg koraka unosa dokumentacije.

Odgovri na pitanja su sljedeća:
1. a) Oko dodavanja podatka o dobavljaču u šifrant kategorija, iz kojeg razloga koristite to polje kada se taj podatak dobavljača može unijeti već kod unosa ponude( u polje "Dovavljač")? 
U opciji Garaniciji povratnog otkupa bi taj podatak unosili u polje "Garant".

b) OT i CV bi unosili u polje Tip garancija. Za CV (i za OT) se može napraviti automatizam izračuna (na dnevnoj, tjednoj ili mjesečnoj bazi) de se azpisuje u polju "Buduća vrijednost".

2. a) Oko dodavanja kolone na pregled partnera možemo nakon što definiramo gdje ćemo evidentirati navedeno.

b) Oko prikaza na mapi ugovora, postoji mogućnost prikaza nove stranice/taba "Dodatni podaci"  (uz 4 postojeće stranice/taba: Ugovor, Ugovor detaljno, Plan otplate i Jamci) i da tamo prikažemo te podatke.

c) iznos reotkupne garancije se može unositi ili izračunavati (navedeno u točci 1. b) u polju "Buduća vrijednost" u opciji Garancija povratnog otkupa.

Molim provjeru i povratnu informaciju, oko detalja me možete kontaktirati na telefon.

Kod garancije povratnog otkupa, na raspolaganju je eksterna funkcija koja se pokrene prilikom promjene tipa garancije na masci za unos/popravak garancije.


